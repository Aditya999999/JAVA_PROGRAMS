package com.aditya;

public class thirteenth {
    public static void main(String[] args){
        int numb = 1;
        {
            //increment
            System.out.println(numb--);
            System.out.println(numb);
        }
        {
            //decrement
            System.out.println(numb++);
            System.out.println(numb);
        }
    }
}
