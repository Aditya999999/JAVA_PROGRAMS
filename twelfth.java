package com.aditya;

public class twelfth {
    public static void main(String[] args){
        //operators
        double a = 1;
        double b = 2;
        double modulo = a % b;
        double add = a + b;
        double diff = a - b;
        double div = a / b;
        System.out.println(modulo);
        System.out.println(add);
        System.out.println(diff);
        System.out.println(div);
    }
}
